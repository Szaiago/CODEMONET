document.getElementById('sub-menu-1').addEventListener('click', function() {
    var submenu = document.getElementById('submenu-list-1');
    if (submenu.style.display === 'block') {
        submenu.style.display = 'none';
    } else {
        submenu.style.display = 'block';
    }
  });
